#include <unistd.h>

int	main(int argv, char **args)
{
	int	i;
	int	j;

	i = argv - 1;
	while (i > 0)
	{
		j = 0;
		while (args[i][j])
		{
			write(1, &args[i][j], 1);
			j++;
		}
		i--;
		write(1, "\n", 1);
	}
}
